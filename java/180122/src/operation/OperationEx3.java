package operation;

public class OperationEx3 {

	public static void main(String[] args) {

		/*
		 * 관계 연산자
		 * 1) < : 작다
		 * 2) > : 크다
		 * 3) == : 같다 (항등 연산자)
		 * 4) != : 같지 않다
		 * 5) >= : 크거나 같다
		 * 6) <= : 작가나 같다
		 * */

		int a = 5;
		int b = 3;

		boolean result = a < b;
		System.out.println(result);

	}// main() end

}//OperationEx3
