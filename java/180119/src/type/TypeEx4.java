package type;

public class TypeEx4 {

	public static void main(String[] args) {
		/*
		 * 1) float : 4 byte (숫자 뒤에 f를 써줘야함)
		 * 2) double : 8 byte (기본 실수)
		 * */
		
		System.out.println(3.14);
		
		// 기본 자료형 float 형 변수 f 를 선언
		float f;
		f = 401.41f;
		System.out.println(f);
		
		// 기본 자료형 double 형 변수 d를 선언하고 값 45.6678 을 대입
		
		double d = 45.6678;
		System.out.println(d);
	}
}
